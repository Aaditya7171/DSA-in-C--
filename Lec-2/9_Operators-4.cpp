//Assignment operators
#include<iostream>
using namespace std;

int main() {
    //(=)
    int a = 5;
    cout<< a <<endl;//Assings the value
    //(+=)
    int b = 4;
    b += 3;
    cout<< b <<endl;//Same as b=b+3
    //(-=)
    int c=6;
    c -= 2;
    cout<< c << endl;//Same as c=c-2
    //(*=)
    int d=6;
    d *= 2;
    cout<< d << endl;//Same as c=c*2
//There are some more assingment operators like {(/=),(%=),(|=),(&=),<<=} etc which are not as useful
}
