//Program for finding positive and negative number--->
#include<iostream>
using namespace std;

int main () {
    int n;
    cout<<"Enter a number to check: ";
    cin>>n;
    if(n > 0) {
        cout<<"Entered number is Positive."<<endl;
    }
    else {
        cout<<"Entered number is Negative."<<endl;;
    }
}
