//Program for Increment & Decrement--->
#include <iostream>
using namespace std;

int main(){
    int i=7;

    cout<< (++i) <<endl;//Pre-increment(i=8)

    cout<< (i++) <<endl;//Post-incerement(i=8 & after execution i = 9)

    cout<< (i--) <<endl;//Post-decerement(i=9 & after execution i = 8)

    cout<< (--i) <<endl;//Pre-decrement(i=7 & i=7)

}
